type JobArgs struct {
	ID          uint64
	IsMap       bool
	MapIndex    int
	ReduceIndex int
}

type JobReply struct {
	ID          uint64
	IsMap       bool
	MapFilename string
	MapIndex    int
	MapCount    int
	ReduceIndex int
	ReduceCount int
}

